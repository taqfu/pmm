<h1 class=' text-center' style="font-weight:bold;">
    How It Works
</h1>
<h2>Create your message.</h2>
<ul>
    <li>
        Write either an email or a message available to the public through a link.
    </li>
    <li>
        Specify how often you want to check in. Every day, every 2 days, every week, every 3 weeks, etc.
    </li>
    <li>
        Specify if you want us to send it out immediately if you don't check in or confirm with you beforehand.
    </li>
    <li>
        And you're all set.
    </li>
</ul>
<h2>Check in.</h2>
<ul>
    <li>
        Once your message is created, you just need to check in by logging in or visiting a link.
    </li>
</ul>
